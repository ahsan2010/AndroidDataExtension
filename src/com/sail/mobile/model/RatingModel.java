package com.sail.mobile.model;

public class RatingModel {

	double totalReviews;
	double totalNegativeReviews;
	double totalPositiveReviews;
	double totalAdReviews;
	double totalAdNegativeReviews;
	double totalAdPositiveReviews;
	
	public double getTotalReviews() {
		return totalReviews;
	}
	public void setTotalReviews(double totalReviews) {
		this.totalReviews = totalReviews;
	}
	public double getTotalNegativeReviews() {
		return totalNegativeReviews;
	}
	public void setTotalNegativeReviews(double totalNegativeReviews) {
		this.totalNegativeReviews = totalNegativeReviews;
	}
	public double getTotalPositiveReviews() {
		return totalPositiveReviews;
	}
	public void setTotalPositiveReviews(double totalPositiveReviews) {
		this.totalPositiveReviews = totalPositiveReviews;
	}
	public double getTotalAdReviews() {
		return totalAdReviews;
	}
	public void setTotalAdReviews(double totalAdReviews) {
		this.totalAdReviews = totalAdReviews;
	}
	public double getTotalAdNegativeReviews() {
		return totalAdNegativeReviews;
	}
	public void setTotalAdNegativeReviews(double totalAdNegativeReviews) {
		this.totalAdNegativeReviews = totalAdNegativeReviews;
	}
	public double getTotalAdPositiveReviews() {
		return totalAdPositiveReviews;
	}
	public void setTotalAdPositiveReviews(double totalAdPositiveReviews) {
		this.totalAdPositiveReviews = totalAdPositiveReviews;
	}
}
